<?php

namespace DotAim\Contact;

final class Contact extends \DotAim\Base\Singleton
{

	/* ===========================================================================
	 * ---------------------------------------------------------------------------
	 * PROPERTIES - START
	 * ---------------------------------------------------------------------------
	 * ======================================================================== */

	/**
	 * @since 1.0.0
	 */
	public $post_types = ['contact_submission'];

	/**
	 * @since 1.0.0
	 */
	public $contact_submission;

	/* ===========================================================================
	 * ---------------------------------------------------------------------------
	 * PROPERTIES - END
	 * ---------------------------------------------------------------------------
	 * ======================================================================== */



	/* ===========================================================================
	 * ---------------------------------------------------------------------------
	 * INIT - START
	 * ---------------------------------------------------------------------------
	 * ======================================================================== */

	/**
	 * @since 1.0.0
	 */
	public function init()
	{

		$this->contact_submission = new Post_Type_Contact_Submission( $this->core );

		// -------------------------------------------------------------------------

		new Taxonomy_Contact_Submission_Category( $this->core );

		// -------------------------------------------------------------------------

		new Meta_Boxes\Meta_Boxes( $this->core );

	}
	// init()

	/* ===========================================================================
	 * ---------------------------------------------------------------------------
	 * INIT - END
	 * ---------------------------------------------------------------------------
	 * ======================================================================== */



	/* ===========================================================================
	 * ---------------------------------------------------------------------------
	 * FRONTEND - START
	 * ---------------------------------------------------------------------------
	 * ======================================================================== */

	/**
	 * @internal
	 */
	public function form_html( $args = [] )
	{

		return $this->contact_submission->form_html( $args );

	}
	// form_html()

	/* ===========================================================================
	 * ---------------------------------------------------------------------------
	 * FRONTEND - END
	 * ---------------------------------------------------------------------------
	 * ======================================================================== */

}
// class Contact
